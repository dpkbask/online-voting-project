At present there is no online system for voting in our country. People have to go
booth and wait into the queue and cast the vote. There is also no online system for requesting voter id. They have contact with the nearby SDO or BDO offices and have to go through with a long process. Casting votes and counting votes are also not a secure process at existing system.
Our aim is to make an online application that will help voters, administrators and the electoral officers. It  help the voters to cast vote from their home. They need not to go outside to give vote in a catastrophic weather. It also give the liberty to the user to choose the protocol (https or local host) through which they want to navigate. It ensures more network security.
Our main objective is:
<ul><li>To provide online voting and registration.</li>
<li>Computerized the activities of casting vote, counting vote, updating and viewing the upcoming election schedules.</li>
<li>Support services in an integrated fashion so as to enable the division Voters, Administrator, Electoral Officers to obtain the relevant information.</li>
